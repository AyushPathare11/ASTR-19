def sum_of_floats():
    x = 2.3
    y = 3.14
    print(x+y)
    print(type(x+y))

def diff_of_ints():
    x = 7
    y = -25
    print(x - y)
    print(type(x - y))

def products_of_comb():
    x = 27
    y = 3.5
    print(x*y)
    print(type(x * y))

def main():
    sum_of_floats()
    diff_of_ints()
    products_of_comb()

if __name__ =="__main__":
    main()